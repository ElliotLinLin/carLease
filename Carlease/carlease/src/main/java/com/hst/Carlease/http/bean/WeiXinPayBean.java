package com.hst.Carlease.http.bean;

public class WeiXinPayBean {
	private String token;// 客户标识
	private String body;// 费用类型
	private String subject;// 标题
	private String total_fee;// 支付金额
	private String ip;// 客户ip
	
	private int contract;// 合同id
	private String dataFrom;//"1"表示合同支付"2"表示租购定金支付

	private String orderNo ;//dataFrom="1 "时，orderNo可为" "；dataFrom="2"时，orderNo为租购合同号 
	
	public String getDataFrom() {
		return dataFrom;
	}

	public void setDataFrom(String dataFrom) {
		this.dataFrom = dataFrom;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getTotal_fee() {
		return total_fee;
	}

	public void setTotal_fee(String total_fee) {
		this.total_fee = total_fee;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public int getContract() {
		return contract;
	}

	public void setContract(int contract) {
		this.contract = contract;
	}


}
