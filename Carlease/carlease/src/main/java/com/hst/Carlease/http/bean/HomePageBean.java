package com.hst.Carlease.http.bean;

/**
 * 首页
 * 
 * @author lyq
 * 
 */
public class HomePageBean {
	private int pageSize;// 页数
	private int pageIndex;// 条数
	private int dicID ;//品牌ID, 
	private double minPrice ;//最低价,
	private double maxPrice ;//最高价, 
	private double minMile  ;//里程最低数, 
	private double maxMile ;//历程最大数
	
	public int getDicID() {
		return dicID;
	}

	public void setDicID(int dicID) {
		this.dicID = dicID;
	}

	public double getMinPrice() {
		return minPrice;
	}

	public void setMinPrice(double minPrice) {
		this.minPrice = minPrice;
	}

	public double getMaxPrice() {
		return maxPrice;
	}

	public void setMaxPrice(double maxPrice) {
		this.maxPrice = maxPrice;
	}

	public double getMinMile() {
		return minMile;
	}

	public void setMinMile(double minMile) {
		this.minMile = minMile;
	}

	public double getMaxMile() {
		return maxMile;
	}

	public void setMaxMile(double maxMile) {
		this.maxMile = maxMile;
	}

	public int getPageSize() {
		return pageSize;
	}

	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}

	public int getPageIndex() {
		return pageIndex;
	}

	public void setPageIndex(int pageIndex) {
		this.pageIndex = pageIndex;
	}

	
}
